
public class Matematica {
	
	public static int somar(int a, int b) { //criaçao de metodos com parametros de dois valores EX: (1 , 2) retornará o valor 3
	return a + b;
	
}
	
	public static int subtrair(int a, int b) { //criaçao de metodos com parametros de dois valores EX: (1 , 2) retornará o valor -1
		return a - b;
		
	}
}

